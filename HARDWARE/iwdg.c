#include "iwdg.h"

//初始化独立看门狗
//prer:分频数:0~7(只有低3位有效!)
//rlr:自动重装载值,0~0XFFF.
//分频因子=4*2^prer.但最大值只能是256!
//rlr:重装载寄存器值:低11位有效.
//时间计算(大概):Tout=((4*2^prer)*rlr)/32 (ms).

//IWDG_Init(4,100); //与分频数为64,重载值为100,溢出时间为200ms	
//IWDG_Init(4,500); //与分频数为64,重载值为500,溢出时间为1s	
//IWDG_Init(6,500); //与分频数为64,重载值为500,溢出时间为4s
//IWDG_Init(6,1000); //与分频数为64,重载值为1000,溢出时间为8s
//IWDG_Init(6,2000); //与分频数为64,重载值为2000,溢出时间为16s
void IWDG_Init(void)
{
	IWDG_WriteAccessCmd(IWDG_WriteAccess_Enable); //使能对IWDG->PR IWDG->RLR的写
	IWDG_SetPrescaler(6); //设置IWDG分频系数
	IWDG_SetReload(1000);  //设置IWDG装载值
	IWDG_ReloadCounter(); //reload
	IWDG_Enable();        //使能看门狗
}
//喂独立看门狗
void IWDG_Feed(void)
{
	IWDG_ReloadCounter();//reload
}
